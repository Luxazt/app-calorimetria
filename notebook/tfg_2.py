def _deduplicar(h, r):
    acum = defaultdict(list)
    for hi, ri in zip(h, r):
        acum[hi].append(ri)
    hu = np.array(sorted(acum.keys()))
    return hu, np.array([np.mean(acum[k]) for k in hu])

