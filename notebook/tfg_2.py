NODOS_MAX = 25   # tope de nodos que quieres tolerar en la fórmula final

def ajustar_gplearn_con_tope(X_tr, y_tr, nodos_max=NODOS_MAX,
                              parsimony_inicial=0.01, intentos=4):
    """gplearn no tiene un tope DURO de nodos (no existe en su API). Como red
    de seguridad, si el programa ganador supera nodos_max, se reintenta con
    una parsimony_coefficient más alta hasta encajar (o agotar los intentos)."""
    parsimony = parsimony_inicial
    est = None
    for intento in range(intentos):
        est = SymbolicRegressor(
            population_size=500, generations=15,
            function_set=['add','sub','mul','div','sqrt','log','inv'],  # sin sin/cos: sin motivo físico aquí
            metric='mse', parsimony_coefficient=parsimony,
            init_depth=(2, 4),                  # árboles iniciales más compactos (antes (2,6))
            p_crossover=0.65, p_subtree_mutation=0.1,
            p_hoist_mutation=0.15,               # ↑ poda subárboles grandes (antes 0.05)
            p_point_mutation=0.1, max_samples=0.9,
            verbose=1, random_state=42, n_jobs=1,
        )
        est.fit(X_tr, y_tr)
        nodos = est._program.length_
        print(f"  Intento {intento+1}: parsimony={parsimony:.4f} → {nodos} nodos")
        if nodos <= nodos_max:
            return est
        parsimony *= 3   # el efecto de parsimony no es lineal; hay que subir fuerte
    print(f"  ⚠️ No se alcanzó el tope de {nodos_max} nodos tras {intentos} intentos "
          f"(quedó en {nodos}). Sube NODOS_MAX o `intentos` si quieres seguir probando.")
    return est

