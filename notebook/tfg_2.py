for horas, col, serie, color in SERIE_COMPLETA:
    if col not in df.columns:
        continue
    mask = df[col].notna()
    n    = mask.sum()
    if n < 12:
        continue
    y_col = df.loc[mask, col].values.astype(float)
    X_col = X_imp[mask.values]

    # Split fijo 80/20: el modelo de holdout NUNCA ve estas muestras al ajustar
    hacer_holdout = n >= N_MIN_HOLDOUT
    if hacer_holdout:
        X_tr, X_te, y_tr, y_te = train_test_split(
            X_col, y_col, test_size=0.2, random_state=42
        )
