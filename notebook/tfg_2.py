def construir_ladrillos(t, D, P, A):
    return pd.DataFrame({
        't': t, 'ln(t+1)': np.log(t+1), '1/t': 1/(t+1e-5), 'sqrt(t)': np.sqrt(t),
        'D': D, 'P_ppal': P, 'P_ace': A,
        'D*ln(t+1)': D*np.log(t+1), 'P_ppal*ln(t+1)': P*np.log(t+1),
        'D*P_ppal': D*P, 'D/sqrt(t)': D/(np.sqrt(t)+1e-5),
    })

