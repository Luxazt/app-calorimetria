# ── 1. Ecuaciones físicas ────────────────────────────────────────────────────
def modelo_hansen(t, R_max, tau, beta):
    t_safe = np.maximum(t, 1e-5)
    return R_max * np.exp(-(tau / t_safe)**beta)

def modelo_gompertz(t, R_max, k, lam):
    return R_max * np.exp(-k * np.exp(-lam * t))

def curva_ce21(horas, res_28d, tipo_cemento='R'):
    """
    CE-21 βcc(t)=exp(s·(1-√(28/t_días))) anclada a 28 d.
    SOLO definida para t ≥ 1 día (rango normativo). Antes de 1 d devuelve NaN
    (no se extrapola): la norma no modela edades tempranas.
    """
    s = {'R': 0.20, 'N': 0.25, 'S': 0.38}.get(tipo_cemento, 0.20)
    horas = np.asarray(horas, dtype=float)
    dias  = horas / 24.0
    with np.errstate(divide='ignore', invalid='ignore'):
        beta = np.where(dias >= 1.0,
                        np.exp(s * (1.0 - np.sqrt(28.0 / np.maximum(dias, 1e-9)))),
                        np.nan)
    return res_28d * beta   # NaN < 1 día

